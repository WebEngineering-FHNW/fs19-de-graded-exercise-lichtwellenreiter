/**
 * These are some Helper Functions for the Map and Character implementation
 */


/**
 * Calculate X Coord
 * @param x
 * @returns {number}
 */
function getXCoord(x) {
    return x * tileW
}

/**
 * Calculate Y Coord
 * @param y
 * @returns {number}
 */
function getYCoord(y) {
    return y * tileH
}